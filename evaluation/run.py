#!/usr/bin/env python3
"""Eris FALSECOLORS local-model evaluation harness, v2.

Iterates over a fixed set of Ollama models and sensitive test documents,
runs the LLM-backed encode/decode round-trip, and records:

  - encode latency
  - decode latency
  - round-trip recovery ratio (difflib)
  - domain mapping size (0 = JSON parse failure)
  - mapping_supported / mapping_unsupported counts from a spot-check of
    sampled (src, tgt) pairs against the cover prose. mapping_unsupported
    detects the "hallucinated mapping" failure mode where the model
    emits a parseable JSON tail that does not correspond to the
    substitutions actually made in the cover.
  - Caudle Distance of cover vs native brewery corpus

Per-model prompt prefix (e.g. "/no_think" for qwen3 family) is set via
OLLAMA_PROMPT_PREFIX in the environment for each call.

Writes results.json (or --out) next to this file. RESULTS.md is
generated by hand from that JSON to keep the writeup human-edited.
"""

import json
import os
import time
import sys
import random
import difflib
import argparse
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))

from falsecolors import (
    encode_document, decode_document, caudle_distance,
    extract_key, EMBED_MARKER,
)

MODELS = [
    "llama3.2:3b",
    "qwen3:1.7b",
    "phi3:mini",
    "gemma3:4b",
    "mistral:7b-instruct",
    "llama3.1:8b",
]

# Per-model prompt prefix. qwen3 family thinks by default and the
# thinking trace bleeds into the cover output; /no_think disables it.
PROMPT_PREFIX = {
    "qwen3:1.7b": "/no_think\n",
    "qwen3:4b": "/no_think\n",
    "qwen3:8b": "/no_think\n",
}

TRIALS = 10
TOPIC = "brewery"
PASSPHRASE = "eval-2026"
MAP_VERIFY_SAMPLES = 5  # number of (src, tgt) pairs to spot-check


def verify_mapping(domain_map, cover_text, k=MAP_VERIFY_SAMPLES, rng=None):
    """Spot-check up to k random (src, tgt) entries. An entry is
    'supported' if its tgt appears as a substring in the cover. Returns
    (supported, unsupported, sample_size). When a model emits a
    parseable JSON tail that doesn't correspond to the substitutions
    actually made, the supported count drops to ~0."""
    items = [(s, t) for s, t in domain_map.items()
             if isinstance(s, str) and isinstance(t, str) and t and s != t]
    if not items:
        return 0, 0, 0
    rng = rng or random
    sample = rng.sample(items, min(k, len(items)))
    supported = sum(1 for _, t in sample if t.lower() in cover_text.lower())
    return supported, len(sample) - supported, len(sample)


def run_one(model, doc_text, corpus_text):
    out = {"model": model}
    os.environ["OLLAMA_PROMPT_PREFIX"] = PROMPT_PREFIX.get(model, "")

    t0 = time.time()
    try:
        cover_full = encode_document(
            doc_text, PASSPHRASE, TOPIC, backend="llm", model=model)
        out["encode_secs"] = round(time.time() - t0, 2)
    except SystemExit:
        out["error"] = "ollama connection refused"
        out["encode_secs"] = round(time.time() - t0, 2)
        return out
    except Exception as e:
        out["error"] = f"encode: {type(e).__name__}: {e}"[:200]
        out["encode_secs"] = round(time.time() - t0, 2)
        return out

    cover_only = cover_full.split(EMBED_MARKER)[0]
    out["cover_chars"] = len(cover_only)

    try:
        _, key_data = extract_key(cover_full, PASSPHRASE)
        domain_map = key_data.get("domain", {}) or {}
        ident_map = key_data.get("ident", {}) or {}
        out["mapping_size"] = len(domain_map)
        out["ident_size"] = len(ident_map)
    except Exception as e:
        out["error"] = f"extract_key: {type(e).__name__}: {e}"[:200]
        return out

    sup, unsup, sampled = verify_mapping(domain_map, cover_only)
    out["mapping_sampled"] = sampled
    out["mapping_supported"] = sup
    out["mapping_unsupported"] = unsup
    out["mapping_supported_ratio"] = (
        round(sup / sampled, 3) if sampled else None)

    scd = caudle_distance(cover_only, corpus_text)
    out["scd"] = (None if scd["scd"] == float("inf") else round(scd["scd"], 4))
    out["doc_pairs"] = scd["doc_pairs"]
    out["doc_mean_cos"] = round(scd.get("doc_mean_cos", 0.0), 3) \
        if "doc_mean_cos" in scd else None

    t1 = time.time()
    try:
        recovered = decode_document(cover_full, PASSPHRASE)
        out["decode_secs"] = round(time.time() - t1, 2)
        out["recovery_ratio"] = round(
            difflib.SequenceMatcher(None, doc_text, recovered).ratio(), 4)
    except Exception as e:
        out["decode_error"] = f"{type(e).__name__}: {e}"[:200]
        out["decode_secs"] = round(time.time() - t1, 2)
        out["recovery_ratio"] = 0.0

    return out


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--models", nargs="*", default=MODELS,
                   help="Override the model list")
    p.add_argument("--trials", type=int, default=TRIALS)
    p.add_argument("--out", default=str(Path(__file__).parent / "results.json"))
    p.add_argument("--resume", action="store_true",
                   help="Load existing --out file and skip any "
                        "(doc, model, trial) already recorded")
    args = p.parse_args()

    corpus = (Path(__file__).parent / "corpora" / "brewery.txt").read_text()
    doc_paths = sorted((Path(__file__).parent / "documents").glob("*.txt"))
    if not doc_paths:
        sys.exit("no documents found under evaluation/documents")

    results = []
    seen = set()
    if args.resume and Path(args.out).exists():
        results = json.loads(Path(args.out).read_text())
        seen = {(r["doc"], r["model"], r["trial"]) for r in results}
        print(f"resume: loaded {len(results)} prior trials from {args.out}")

    started = time.time()
    total = len(doc_paths) * len(args.models) * args.trials
    n = 0
    for doc_path in doc_paths:
        doc_text = doc_path.read_text()
        for model in args.models:
            for trial in range(args.trials):
                n += 1
                if (doc_path.name, model, trial + 1) in seen:
                    continue
                tag = f"[{n}/{total}] {doc_path.name} | {model} | t{trial+1}"
                print(tag, "...", end=" ", flush=True)
                r = run_one(model, doc_text, corpus)
                r["doc"] = doc_path.name
                r["trial"] = trial + 1
                results.append(r)
                if "error" in r:
                    print(f"FAIL ({r['error'][:50]})")
                else:
                    rr = r.get("recovery_ratio", 0)
                    ms = r.get("mapping_size", 0)
                    sup = r.get("mapping_supported", 0)
                    samp = r.get("mapping_sampled", 0)
                    sc = r.get("scd")
                    print(f"recovery={rr:.3f} map={ms} sup={sup}/{samp} "
                          f"scd={sc} enc={r['encode_secs']}s")
                Path(args.out).write_text(json.dumps(results, indent=2))

    elapsed = round(time.time() - started, 1)
    print(f"\nDone. {len(results)} trials in {elapsed}s. Wrote {args.out}")


if __name__ == "__main__":
    main()
